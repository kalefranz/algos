"""
1730. Shortest Path to Get Food
Medium

You are starving and you want to eat food as quickly as possible. You want to find the shortest path to arrive at any food cell.

You are given an m x n character matrix, grid, of these different types of cells:

    '*' is your location. There is exactly one '*' cell.
    '#' is a food cell. There may be multiple food cells.
    'O' is free space, and you can travel through these cells.
    'X' is an obstacle, and you cannot travel through these cells.

You can travel to any adjacent cell north, east, south, or west of your current location if there is not an obstacle.

Return the length of the shortest path for you to reach any food cell. If there is no path for you to reach food, return -1.

Topics: Array, BFS, Matrix

https://leetcode.com/problems/shortest-path-to-get-food/

"""


from collections import deque
from typing import *


class Solution:
    def getFood(self, grid: List[List[str]]) -> int:
        rows, cols = len(grid), len(grid[0])
        start = next((
            (r, c) for r in range(rows) for c in range(cols) if grid[r][c] == '*'
        ), None)
        # assert start, start

        queue = deque()
        visited = set()
        visited.add(start)
        queue.append(
            # steps, (r, c)
            (0, start)
        )
        while queue:
            steps, (r, c) = queue.popleft()
            # print(steps, (r, c), queue)
            steps += 1
            for (rp, cp) in ((r + 1, c), (r - 1, c), (r, c + 1), (r, c - 1)):
                if 0 <= rp < rows and 0 <= cp < cols:
                    if (rp, cp) not in visited:
                        val = grid[rp][cp]
                        if val == '#':
                            return steps
                        elif val == 'O':
                            # add to queue and visited
                            visited.add((rp, cp))
                            queue.append(
                                (steps, (rp, cp))
                            )
                        elif val == 'X':
                            # add to visisted
                            visited.add((rp, cp))
        return -1


